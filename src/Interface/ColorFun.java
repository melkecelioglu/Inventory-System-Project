    //methos to assign and use  R G B values
    private void colorChoose() {
        int R, G, B;
        R = slider_red.getValue();
        G = slider_green.getValue();
        B = slider_blue.getValue();
        colorPanel.setBackground(new Color(R, G, B));
        spinner_Blue.setValue(slider_blue.getValue());
        spinner_green.setValue(slider_green.getValue());
        spinner_red.setValue(slider_red.getValue());
        
    }

