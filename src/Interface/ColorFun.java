    private void rbtn_previewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtn_previewActionPerformed
        //to change the color of panel we need to use Window object
        Window parentWindow = SwingUtilities.windowForComponent(rbtn_preview);
        //to see synchronised color changes, use changelistener 
        //set the color for panel to see it 
        JColorChooser jcc = new JColorChooser();
        jcc.getSelectionModel().addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                panelColor.setBackground(jcc.getColor());
            }
        });
        //JDialog to control represents a top level window 
        JDialog dialog = new JDialog(parentWindow);
        dialog.add(jcc);
        dialog.pack();
        dialog.setVisible(true);
    }//GEN-LAST:event_rbtn_previewActionPerformed

